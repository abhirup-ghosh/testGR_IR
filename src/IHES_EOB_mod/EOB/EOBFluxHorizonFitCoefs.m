function c = EOBFluxHorizonFitCoefs(nu, EOBopt)

%EOBFluxHorizonFitCoefs Coefficients for hybrid expression of rho_lm22 at the horizon.
%
%    c = EOBFluxHorizonFitCoefs(nu, EOBopt)
